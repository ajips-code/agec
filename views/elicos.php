    <script>
        document.addEventListener('DOMContentLoaded', () => {
            const observerOptions = {
                threshold: 0.1
            };

            const observer = new IntersectionObserver((entries, observer) => {
                entries.forEach(entry => {
                    if (entry.isIntersecting) {
                        entry.target.classList.add('visible');
                        observer.unobserve(entry.target);
                    }
                });
            }, observerOptions);

            const elementsToAnimate = document.querySelectorAll('.skills-container, .skills-image-section, .skills-caption, .skills-content-section, .skills-buttons');
            elementsToAnimate.forEach(element => {
                observer.observe(element);
            });
        });

        document.addEventListener('DOMContentLoaded', () => {
            const countrySelect = document.getElementById('countrySelect');
            const goalSelect = document.getElementById('goalSelect');
            const intakeSelect = document.getElementById('intakeSelect');

            if (!countrySelect || !goalSelect || !intakeSelect) {
                console.error("One or more elements are missing from the DOM");
                return;
            }

            function resetSelections() {
                countrySelect.value = "";
                goalSelect.value = "";
                intakeSelect.value = "";
                localStorage.removeItem('elicosCountry');
                localStorage.removeItem('elicosGoal');
                localStorage.removeItem('elicosIntake');
            }

            function saveToLocalStorage() {
                const country = countrySelect.value;
                const goal = goalSelect.value;
                const intake = intakeSelect.value;

                if (country && goal && intake) {
                    localStorage.setItem('elicosCountry', country);
                    localStorage.setItem('elicosGoal', goal);
                    localStorage.setItem('elicosIntake', intake);

                    // Log the saved values to the console
                    console.log('Saved to localStorage:');
                    console.log('Country:', localStorage.getItem('elicosCountry'));
                    console.log('Goal:', localStorage.getItem('elicosGoal'));
                    console.log('Intake:', localStorage.getItem('elicosIntake'));

                    // Redirect to elicos-form2.php
                    window.location.href = "elicos-form2.php";
                }
            }

            function checkSelections() {
                const country = countrySelect.value;
                const goal = goalSelect.value;
                const intake = intakeSelect.value;

                if (country && goal && intake) {
                    saveToLocalStorage();
                }
            }

            countrySelect.addEventListener('change', checkSelections);
            goalSelect.addEventListener('change', checkSelections);
            intakeSelect.addEventListener('change', checkSelections);

            // Reset selections and clear local storage on page load
            resetSelections();
        });



